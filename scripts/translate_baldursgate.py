# More translations for common Baldur's Gate words
with open("C:/Users/R6X/opencode/klepanka/js/decks/baldursgate.js", "r", encoding="utf-8") as f:
    content = f.read()

# Find all bracketed entries
pattern = re.compile(r'(en: ".*?", pl: )"\[(.*?)\]"')

untranslated_words = set()
for m in re.finditer(r'en: "(.*?)", pl: "\[(.*?)\]"', content):
    untranslated_words.add(m.group(1))

# Transliterate what we can
missing = [w for w in sorted(untranslated_words) if w not in TRANSLATIONS]
print(f"Words needing translation: {len(untranslated_words)} total, {len(missing)} still missing")
print("Sample missing:", missing[:30] if missing else "none")

# Apply translations
count = 0
def replace_match(m):
    global count
    en_word = m.group(2)
    if en_word in TRANSLATIONS:
        count += 1
        return f'{m.group(1)}"{TRANSLATIONS[en_word]}"'
    return m.group(0)

new_content = pattern.sub(replace_match, content)

if new_content != content:
    out_path = "C:/Users/R6X/opencode/klepanka/js/decks/baldursgate.js"
    with open(out_path, "w", encoding="utf-8") as f:
        f.write(new_content)
    print(f"Applied {count} translations")
else:
    print("No changes made")

# Still missing after translation?
remaining = set()
for m in re.finditer(r'en: "(.*?)", pl: "\[(.*?)\]"', new_content):
    remaining.add(m.group(1))
print(f"Still untranslated after pass: {len(remaining)}")
if remaining:
    print("Remaining sample:", sorted(remaining)[:40])
